/***
     * En este metodo primero se recoore la lista based de la clase Bd para verificar que el nombre de la  tabla a crear 
     * no se repita y si el nombre de la tabla se repite se enciende la bandera repetida y se arroja un mensaje diciendo que 
     * el nombre de la tabla ya existe, en caso contrario se recorre el ArrayList alTextos y se le agrega a la lista encabezado 
     * un objeto de tipo Campo que se crea pasandole al constructor el nombre y el tipo de dato del campo.
     * @param evt 
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Tabla t;
        ListaLigada encabezado = new ListaLigada();
        boolean repetida = false;
        
        for(int i = 0; i < Bd.based.size(); i++)
            if(((Tabla) Bd.based.get(i).getDato()).getNombre().equalsIgnoreCase(jTextField1.getText())){
                repetida = true;
                break;
            }
        
        if(!repetida){
            t = new Tabla(jTextField1.getText());

            for(int i = 0; i < alTextos.size(); i++)
                encabezado.agregar(new Campo(alTextos.get(i).getText(), alTipos.get(i).getSelectedIndex()));

            t.getRegistros().agregar(encabezado);
            Bd.based.agregar(t);

            this.dispose();
        }
        else
            JOptionPane.showMessageDialog(this, "El nombre de tabla ya existe.");
    }//GEN-LAST:event_jButton2ActionPerformed

